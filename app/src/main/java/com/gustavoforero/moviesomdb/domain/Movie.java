package com.gustavoforero.moviesomdb.domain;

public class Movie {

    private String Title;
    private String Type;
    private String Poster;
    private String Year;

    public String getTitle() {
        return Title;
    }

    public String getType() {
        return Type;
    }

    public String getPoster() {
        return Poster;
    }

    public String getYear() {
        return Year;
    }
}
